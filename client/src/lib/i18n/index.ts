export function t(lang: LanguageCode, key: string): string {
  return strings[lang]?.[key] || strings["en"][key] || key;
}

export function getDirection(lang: LanguageCode): "ltr" | "rtl" {
  return LANGUAGES[lang]?.direction || "ltr";
}

export function isRTL(lang: LanguageCode): boolean {
  return LANGUAGES[lang]?.direction === "rtl";
}

export function getLanguageList(): LanguageInfo[] {
  return Object.values(LANGUAGES);
}

export function detectBrowserLanguage(): LanguageCode {
  if (typeof navigator === "undefined") return "en";
  
  const browserLang = navigator.language.split("-")[0].toLowerCase();
  const supported = Object.keys(LANGUAGES) as LanguageCode[];
  
  if (supported.includes(browserLang as LanguageCode)) {
    return browserLang as LanguageCode;
  }
  
  return "en";
}

export default { t, LANGUAGES, getDirection, isRTL, getLanguageList, detectBrowserLanguage };
